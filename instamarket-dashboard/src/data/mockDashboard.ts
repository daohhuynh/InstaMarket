function makeChart(seed: number, start: number, drift = 0): ChartPoint[] {
  const labels = [
    "09:30",
    "10:10",
    "10:50",
    "11:30",
    "12:10",
    "12:50",
    "13:30",
    "14:10",
    "14:50",
    "15:30",
    "16:10",
    "16:50",
    "17:30",
    "18:10",
    "18:50",
    "19:30",
  ];

  let current = start;
  return labels.map((label, index) => {
    const wave = Math.sin((index + 1) * (seed * 0.31)) * 2.9;
    const pulse = Math.cos((index + 1) * (seed * 0.17)) * 1.8;
    current = Math.max(3, Math.min(97, current + wave + pulse + drift));
    return {
      time: label,
      label,
      probability: Number(current.toFixed(1)),
      volume: Math.round(650 + index * 75 + seed * 41),
    };
  });
}

function market(
  id: string,
  data: Omit<MarketRecord, "id">,
): MarketRecord {
  return { id, ...data };
}

function sourceMetric(
  source: string,
  winRate: number,
  edgeCaptured: number,
  bets: number,
  confidence: number,
  conversion: number,
  trendPoints: number[],
  thesis: string,
  accent: string,
): SourceMetric {
  return {
    source,
    winRate,
    edgeCaptured,
    bets,
    confidence,
    conversion,
    trendPoints,
    thesis,
    accent,
  };
}

function activity(
  id: string,
  type: ActivityEvent["type"],
  label: string,
  timestamp: string,
  direction: ActivityEvent["direction"],
  amount: string,
  source: string,
): ActivityEvent {
  return { id, type, label, timestamp, direction, amount, source };
}

